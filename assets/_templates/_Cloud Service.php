                    <h3>What defines a cloud service :</h3>
                    <br>
                    <ul>
                        <li>Cloud services are infrastructure, platforms, or software that are hosted by third-party providers and made available to users through the internet.</li>
                        <br>
                        <li>They’re made available to customers from the providers' servers, so there's no need for a company to host applications on its own on-premises servers.</li>
                        <br>
                        <li>organizations must also decide which type of environment works best for the business: public cloud, private cloud, or a mix of both.</li>
                        <br>
                        <li>Cloud services facilitate the flow of user data from front-end clients (e.g., users’ servers, tablets, desktops, laptops—anything on the users’ ends)</li>
                        <br>
                        <li>through the internet, to the provider’s systems, and back.</li>
                        <br>
